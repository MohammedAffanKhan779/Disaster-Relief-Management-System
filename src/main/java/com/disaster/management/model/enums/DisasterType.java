package com.disaster.management.model.enums;

/**
 * Enumeration for Disaster Types
 * Categorizes different types of disasters
 */
public enum DisasterType {
    FLOOD,          // Water-related disasters
    EARTHQUAKE,     // Seismic disasters
    CYCLONE,        // Storm-related disasters
    LANDSLIDE,      // Land movement disasters
    FIRE,           // Fire-related disasters
    PANDEMIC,       // Health crisis disasters
    OTHER           // Other types of disasters
}
